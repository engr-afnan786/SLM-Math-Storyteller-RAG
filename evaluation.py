#!/usr/bin/env python3
"""
Evaluate RAG vs Non-RAG: analyze logs + run automated A/B simulation.
Usage:
    python evaluation.py              # Analyze existing logs
    python evaluation.py --run        # Run A/B simulation
    python evaluation.py --run --turns 15
"""

import os, sys, json, time, argparse
import numpy as np
from pathlib import Path

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
RESULTS_DIR = os.path.join(SCRIPT_DIR, "evaluation_results")

# ── Load JSONL ──

def load_jsonl(path):
    entries = []
    if not os.path.exists(path):
        return entries
    with open(path) as f:
        for line in f:
            line = line.strip()
            if line:
                try: entries.append(json.loads(line))
                except: pass
    return entries

# ── Analyze Entries ──

def analyze_entries(entries, label):
    from engine import verify_math
    total_facts = correct = incorrect = 0
    latencies = []
    for e in entries:
        mm = e.get("math_metrics", {})
        if mm and mm.get("total_facts", 0) > 0:
            total_facts += mm["total_facts"]
            correct += mm.get("correct", 0)
            incorrect += mm.get("incorrect", 0)
        else:
            _, _, m2 = verify_math(e.get("ai_response", ""))
            total_facts += m2["total_facts"]
            correct += m2["correct"]
            incorrect += m2["incorrect"]
        lat = e.get("latency_seconds", e.get("latency", 0))
        if lat: latencies.append(float(lat))

    err = round(incorrect / total_facts, 4) if total_facts > 0 else 0.0
    avg_lat = round(sum(latencies)/len(latencies), 2) if latencies else 0

    # Coherence from logs
    drifts = [e.get("coherence_metrics", {}).get("topic_drift", 0)
              for e in entries if e.get("coherence_metrics", {}).get("topic_drift")]
    drift_avg = round(np.mean(drifts), 4) if drifts else "N/A"
    drift_max = round(np.max(drifts), 4) if drifts else "N/A"
    trend = 0.0
    if len(drifts) >= 4:
        trend = round(float(np.polyfit(np.arange(len(drifts)), np.array(drifts), 1)[0]), 4)

    print(f"\n  [{label}]")
    print(f"    Entries: {len(entries)}")
    print(f"    Math: {total_facts} facts, {correct} correct, {incorrect} incorrect (err={err})")
    print(f"    Latency: avg {avg_lat}s")
    print(f"    Drift: avg={drift_avg}, max={drift_max}, trend={trend}")

    return {"entries": len(entries), "total_facts": total_facts, "correct": correct,
            "incorrect": incorrect, "error_rate": err, "avg_latency": avg_lat,
            "avg_drift": drift_avg, "max_drift": drift_max, "drift_trend": trend}

# ── Analyze Logs ──

def analyze_logs():
    print("\n" + "=" * 60)
    print("  LOG ANALYSIS")
    print("=" * 60)

    rag = analyze_entries(load_jsonl(os.path.join(SCRIPT_DIR, "With_RAG.json")), "RAG")
    nr = analyze_entries(load_jsonl(os.path.join(SCRIPT_DIR, "Without_RAG.json")), "Non-RAG")

    print(f"\n{'Metric':<25} {'RAG':>10} {'Non-RAG':>10}")
    print("-" * 47)
    for k, l in [("error_rate","Math Err Rate"), ("avg_latency","Avg Latency(s)"),
                 ("total_facts","Total Facts"), ("correct","Correct"),
                 ("avg_drift","Avg Drift"), ("drift_trend","Drift Trend")]:
        print(f"{l:<25} {str(rag.get(k,'N/A')):>10} {str(nr.get(k,'N/A')):>10}")

    os.makedirs(RESULTS_DIR, exist_ok=True)
    with open(os.path.join(RESULTS_DIR, "log_analysis.json"), "w") as f:
        json.dump({"rag": rag, "non_rag": nr}, f, indent=2, default=str)

    _gen_report(rag, nr)
    return rag, nr

